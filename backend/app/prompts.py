"""国自然基金申请书四大文本能力的提示词构建。

每个模块把用户的结构化输入拼成一组 messages 交给 LLM。
设计原则:
  - 角色化(评审专家/方法学专家), 给出明确产出结构, 降低普通用户使用门槛;
  - 合规优先: 提示词始终强调“辅助而非代写”, 不得编造文献与数据;
  - 贴合国自然评审逻辑(创新性、科学问题属性、可行性、研究基础)。
"""
from __future__ import annotations

from .compliance import build_annotation

# 所有模块共享的合规底线, 注入到 system 中。
_COMPLIANCE = (
    "重要合规要求：你是辅助工具，不能代替申请人完成实质科学创作；"
    "严禁编造任何参考文献、数据或事实；凡涉及具体文献，须提示申请人自行核实真实性；"
    "输出为可供申请人修改的草稿与建议，不得声称这是可直接提交的最终申请书。"
)


def _join(label: str, value: str) -> str:
    value = (value or "").strip()
    return f"{label}：{value}\n" if value else ""


# ------------------------- 1. 选题诊断 -------------------------

def build_critique(inputs: dict) -> list[dict]:
    system = (
        "你是国家自然科学基金的资深评审专家，熟悉面上/青年/地区项目的评审标准。"
        + _COMPLIANCE
        + "请基于申请人提供的选题信息，做一次严格的“选题诊断”，按以下结构输出：\n"
        "## 一、创新性评估\n判断创新点是否够“硬”：是真问题还是伪命题？是原始创新、集成创新还是简单跟随？"
        "明确指出创新性强/中/弱并说明理由。\n"
        "## 二、科学问题属性归类\n依据基金委 2024 年起施行的两类研究属性（已取代旧的 A/B/C/D 四类），"
        "判断该选题更契合哪一类并给出推荐与理由：\n"
        "（1）自由探索类基础研究：源于科研人员好奇心或创新性学术灵感、不以满足现阶段应用需求为目的的"
        "原创性、前沿性基础研究，评审以“原创性、前沿性”为首要标准；\n"
        "（2）目标导向类基础研究：以经济社会发展需要或国家需求为牵引的基础研究，科学问题须来源于实践，"
        "评审以“国家需求、社会需要”为基石。\n"
        "## 三、与已资助方向的潜在重叠\n基于常识提示该方向可能已有较多布局之处，"
        "并建议申请人到基金委官网“项目查询”自查近年立项，避免撞题（不要编造具体已立项项目）。\n"
        "## 四、3 个最可能被毙的硬伤\n以挑剔评审视角列出最致命的三条问题（如科学问题不聚焦、"
        "创新性不足、可行性存疑、工作量过大/过小等），每条配一句可操作的改进方向。\n"
        "## 五、一句话结论\n给出“建议推进 / 需重大调整 / 建议更换”的明确判断。\n"
        "语言犀利、具体、就事论事，避免空话套话。"
    )
    user = (
        _join("拟申报项目名称/选题", inputs.get("title", ""))
        + _join("研究领域/学科", inputs.get("field", ""))
        + _join("拟解决的科学问题", inputs.get("problem", ""))
        + _join("自认为的创新点", inputs.get("innovation", ""))
        + _join("已有研究基础/条件", inputs.get("background", ""))
    )
    return [{"role": "system", "content": system}, {"role": "user", "content": user}]


# ------------------------- 2. 研究方案(目标·内容·关键问题·技术路线) -------------------------

def build_scheme(inputs: dict) -> list[dict]:
    system = (
        "你是国家自然科学基金申请书写作方法学专家。"
        + _COMPLIANCE
        + "请把申请人的研究构想组织成逻辑自洽、层层对应的“研究方案”，按以下结构输出：\n"
        "## 一、研究目标\n凝练 1-3 条总目标，聚焦科学问题，避免罗列任务。\n"
        "## 二、研究内容\n3-5 项研究内容，每项明确对应哪个研究目标，避免内容与目标错位。\n"
        "## 三、拟解决的关键科学问题\n从研究内容中抽炼 1-3 个真正的“科学问题”（不是技术任务），"
        "说明其难点与科学价值。\n"
        "## 四、技术路线\n给出从输入到产出的研究技术路线，分阶段描述方法与逻辑衔接；"
        "并额外提供一段 Mermaid 流程图代码（```mermaid ... ```，flowchart TD），便于绘图。\n"
        "## 五、可行性分析\n从研究基础、技术条件、方法成熟度三方面论证可行性，"
        "并指出 2-3 个潜在风险及应对预案。\n"
        "## 六、年度研究计划\n把研究内容按申请执行期（如 4 年）分解为逐年任务与阶段性节点，"
        "用 Markdown 表格呈现（表头：| 年度 | 主要研究任务 | 阶段性目标/节点 |），各年任务与上面的研究内容对应。\n"
        "## 七、预期研究结果\n列出可考核的成果（如学术论文、发明专利、方法/数据集、人才培养、学术交流等），"
        "数量与水平务实合理，不夸大、不承诺无法兑现的指标。\n"
        "## 八、自检提示\n指出当前构想中目标—内容—关键问题之间是否存在断点或不匹配，需申请人补强之处。\n"
        "表达分点、清晰、可落地。"
    )
    user = (
        _join("研究构想/课题", inputs.get("idea", ""))
        + _join("研究领域/学科", inputs.get("field", ""))
        + _join("已有研究目标(可选)", inputs.get("objective", ""))
        + _join("可用资源/研究基础", inputs.get("resources", ""))
    )
    return [{"role": "system", "content": system}, {"role": "user", "content": user}]


# ------------------------- 3. 评审模拟 -------------------------

def build_review(inputs: dict) -> list[dict]:
    system = (
        "你是国家自然科学基金会评/通讯评审的模拟器。"
        + _COMPLIANCE
        + "请扮演三位背景不同的评审专家，对申请人提供的申请书内容(或摘要)分别给出独立评审意见：\n"
        "### 评审一：本领域同行专家\n关注科学问题、创新性与学术价值。\n"
        "### 评审二：交叉学科专家\n关注表述清晰度、逻辑自洽与对非小同行的可读性。\n"
        "### 评审三：严苛的“挑刺型”评审\n专找可行性、工作量、研究基础、技术路线中的漏洞。\n"
        "每位评审都需包含：① 主要优点（1-2 条）；② 主要问题（2-3 条，具体到位）；"
        "③ 综合评价等级（优 / 良 / 中 / 差）；④ 是否建议资助。\n"
        "全部评审意见后，依次另起以下两节：\n"
        "## 评分汇总\n用一个 Markdown 表格汇总三位评审在五个维度上的评分（每格用 优/良/中/差），"
        "表头为：| 维度 | 评审一 | 评审二 | 评审三 |，五个维度依次为："
        "科学问题凝练、创新性、研究基础、方案可行性、写作规范。\n"
        "## 共识弱点（按严重度排序）\n综合三位评审，列出他们普遍认同、最可能导致不予资助的核心弱点，"
        "按严重度从高到低编号排序，每条给出一句可操作的修改优先级建议。\n"
        "意见要具体、尖锐、可操作，模拟真实评审的严格程度。"
    )
    user = (
        _join("项目名称(可选)", inputs.get("title", ""))
        + "以下是申请书内容(或核心摘要)：\n\n"
        + (inputs.get("text", "") or "").strip()
    )
    return [{"role": "system", "content": system}, {"role": "user", "content": user}]


# ------------------------- 4. 润色与合规标注 -------------------------

def build_polish(inputs: dict) -> list[dict]:
    annotation = build_annotation()
    system = (
        "你是熟悉国家自然科学基金申请书行文规范的学术编辑。"
        + _COMPLIANCE
        + "请对申请人提供的文本做“润色”而非“改写内容”：\n"
        "① 提升为规范、凝练、有学术性的基金书面语，去除口语与空泛套话；\n"
        "② 增强逻辑连接与段落层次，但不得增删实质科学内容、不得编造数据或文献；\n"
        "③ 统一术语、修正语病与标点。\n"
        "输出结构：\n## 一、润色后的文本\n（给出润色后的完整文本）\n"
        "## 二、主要修改说明\n（列出你做的关键调整，以及需要申请人自行确认/补充之处）\n"
        "## 三、生成式 AI 使用标注（按基金委要求附于材料）\n"
        f"（在此给出一段可据实修改后采用的标注文字，模板参考：{annotation}）\n"
        "注意：标注需提醒申请人据实修改 AI 工具的名称、版本、使用时间与使用环节。"
    )
    user = "以下是待润色文本：\n\n" + (inputs.get("text", "") or "").strip()
    return [{"role": "system", "content": system}, {"role": "user", "content": user}]


# ------------------------- 6. 项目摘要(从全文凝练) -------------------------

def build_abstract(inputs: dict) -> list[dict]:
    system = (
        "你是国家自然科学基金申请书写作专家。"
        + _COMPLIANCE
        + "请基于申请人提供的申请书内容，凝练出符合基金委要求的“项目摘要”。"
        "铁律：只压缩与提炼已有内容，不新增观点、不编造数据或文献；"
        "摘要须能独立成段、概括全文。按以下结构输出：\n"
        "## 中文摘要\n一段约 400 字的摘要，依次涵盖：研究背景与意义、拟解决的关键科学问题、"
        "主要研究内容与方法、预期成果与价值；语言凝练客观，避免空泛套话。\n"
        "## 关键词\n3-5 个，用分号分隔。\n"
        "## Abstract\n与中文摘要对应的英文摘要。\n"
        "## Keywords\n3-5 个英文关键词，用分号分隔。\n"
        "若提供内容不足以支撑某部分，请明确提示申请人补充，不要编造。"
    )
    user = "以下是申请书核心内容（可能由多节拼接）：\n\n" + (inputs.get("text", "") or "").strip()
    return [{"role": "system", "content": system}, {"role": "user", "content": user}]


# ------------------------- 7. 据评审生成修订建议(闭环) -------------------------

def build_revise(inputs: dict) -> list[dict]:
    system = (
        "你是国家自然科学基金申请书修改专家。"
        + _COMPLIANCE
        + "下面给你“申请书草稿”与“模拟评审意见（含共识弱点）”。请据评审意见，"
        "为申请人给出可操作的修订建议，要求：\n"
        "① 针对评审指出的每一条主要问题/共识弱点，逐条处理；\n"
        "② 每条都要：复述该问题 → 定位到草稿中最相关的部分（引用关键句或小标题）→ "
        "给出具体、可执行的修改建议（怎么改、补什么）；\n"
        "③ 严禁替申请人编造数据、文献或结论；不确定处提示其自行补充与核实；\n"
        "④ 最后另起“## 修改优先级”：列出先改哪几条最影响评审结果。\n"
        "用 Markdown 分条输出，具体、可落地。"
    )
    user = (
        "【申请书草稿】\n"
        + (inputs.get("text", "") or "").strip()
        + "\n\n【模拟评审意见】\n"
        + (inputs.get("review", "") or "").strip()
    )
    return [{"role": "system", "content": system}, {"role": "user", "content": user}]


_BUILDERS = {
    "critique": build_critique,
    "scheme": build_scheme,
    "review": build_review,
    "polish": build_polish,
    "abstract": build_abstract,
    "revise": build_revise,
}


def build_messages(module: str, inputs: dict) -> list[dict]:
    builder = _BUILDERS.get(module)
    if not builder:
        raise ValueError(f"未知模块: {module}")
    return builder(inputs)
